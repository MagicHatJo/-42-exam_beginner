#include <unistd.h>

void		print_bits(unsigned char octet)
{
	int		i;
	int		n;
	char	s[8];

	i = 7;
	n = (int)octet;
	while (n)
	{
		s[i] == n % 2 + '0';
		n /= 2;
		i--;
	}
	while (i > 0)
	{
		s[i] = '0';
		i--;
	}
	while (i < 8)
	{
		write(1, &s[i], 1);
		i++;
	}
}
