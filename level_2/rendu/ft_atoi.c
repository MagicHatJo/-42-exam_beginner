int		ft_isspace(char c)
{
	return (c == '\t' || c == '\n' || c == '\v' ||
			c == '\f' || c == '\r' || c == ' ' ? 1 : 0);
}

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int s;

	i = 0;
	n = 0;
	s = 1;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '+' || str[i] == '-')
		s = (str[i] == '-' ? -1 : 1);
	while (str[++i] >= '0' && str[i] <= '9')
		n = n * 10 + str[i] - '0';
	return (n * s);
}
