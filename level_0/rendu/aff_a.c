#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;

	i = 0;
	while (argc == 2 && argv[1][i] && argv[1][i] != 'a')
		i++;
	if (argc != 2 || argv[1][i])
		write(1, "a", 1);
	write(1, "\n", 1);
	return (0);
}
